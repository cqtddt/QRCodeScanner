package cn.hugo.android.scanner.create;

public class ShowInfo {

	private String storeName;
	private String userName;
	private String weChatHint;
	private String prodName;
	private String prodPrice;
	private String prodActivityPrice;
	private String qrCodeHint;
	private String logoName;
	private String prodUrl;
	
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getWeChatHint() {
		return weChatHint;
	}
	public void setWeChatHint(String weChatHint) {
		this.weChatHint = weChatHint;
	}
	public String getProdName() {
		return prodName;
	}
	public void setProdName(String prodName) {
		this.prodName = prodName;
	}
	public String getProdPrice() {
		return prodPrice;
	}
	public void setProdPrice(String prodPrice) {
		this.prodPrice = prodPrice;
	}
	public String getProdActivityPrice() {
		return prodActivityPrice;
	}
	public void setProdActivityPrice(String prodActivityPrice) {
		this.prodActivityPrice = prodActivityPrice;
	}
	public String getQrCodeHint() {
		return qrCodeHint;
	}
	public void setQrCodeHint(String qrCodeHint) {
		this.qrCodeHint = qrCodeHint;
	}
	public String getLogoName() {
		return logoName;
	}
	public void setLogoName(String logoName) {
		this.logoName = logoName;
	}
	public String getProdUrl() {
		return prodUrl;
	}
	public void setProdUrl(String prodUrl) {
		this.prodUrl = prodUrl;
	}
}
